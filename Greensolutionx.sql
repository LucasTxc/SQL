);

CREATE TABLE PROJETOS (
    CodProJ INTEGER PRIMARY KEY,
    Nome VARCHAR(50),
    Area VARCHAR(50),
    Data DATE,
    Criador VARCHAR(50),
    Descricao BLOB
);

